"""
Escribir un programa que permita ingresar dos cadenas de caracteres e indicar si son iguales o distintas.
"""

cadena1 = input("Cadena 1 => ")
cadena2 = input("Cadena 2 => ")

if cadena1 != cadena2:
    print(f"La cadena '{cadena1}', no es igual a la cadena '{cadena2}'")
else:
    print(f"La cadena '{cadena1}',es igual a la cadena '{cadena2}'")
